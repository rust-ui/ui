class MarqueePage extends BasePage {
  protected readonly componentName = "marquee";

  // Marquee elements
  readonly marqueeWrapper: Locator;
  readonly marquees: Locator;
  readonly marqueeRows: Locator;
  readonly figures: Locator;
  readonly avatars: Locator;
  readonly blockquotes: Locator;

  constructor(page: Page) {
    super(page);

    this.marqueeWrapper = this.firstByDataName("MarqueeWrapper");
    this.marquees = this.byDataName("Marquee");
    this.marqueeRows = this.byDataName("MarqueeRow");
    // Scoped within preview
    this.figures = this.preview.locator("figure");
    this.avatars = this.preview.locator('img[src*="avatar"]');
    this.blockquotes = this.preview.locator("blockquote");
  }
}

