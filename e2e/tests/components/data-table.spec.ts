class DataTablePage extends BasePage {
  protected readonly componentName = "data-table";

  // Toolbar elements
  readonly filterInput: Locator;
  readonly columnsSelect: Locator;

  // Table elements (semantic HTML)
  readonly table: Locator;
  readonly tableHeader: Locator;
  readonly tableBody: Locator;
  readonly headerCells: Locator;
  readonly dataRows: Locator;

  // Checkboxes
  readonly checkboxes: Locator;

  // Sort button
  readonly emailSortButton: Locator;

  // Footer
  readonly selectionInfo: Locator;
  readonly previousButton: Locator;
  readonly nextButton: Locator;

  constructor(page: Page) {
    super(page);

    // Toolbar - scoped within preview
    this.filterInput = this.preview.getByRole("textbox", { name: "Filter emails..." });
    this.columnsSelect = this.preview.getByRole("button", { name: /selected/ });

    // Table (semantic HTML table elements) - scoped within preview
    this.table = this.preview.getByRole("table").first();
    this.tableHeader = this.table.locator("thead");
    this.tableBody = this.table.locator("tbody");
    this.headerCells = this.table.getByRole("columnheader");
    this.dataRows = this.tableBody.getByRole("row");

    // Checkboxes - scoped within preview
    this.checkboxes = this.preview.getByRole("checkbox");

    // Sort - scoped within preview
    this.emailSortButton = this.preview.getByRole("button", { name: /Email/ });

    // Footer - scoped within preview
    this.selectionInfo = this.preview.locator("text=/\\d+ of \\d+ row/");
    this.previousButton = this.preview.getByRole("button", { name: "Previous" });
    this.nextButton = this.preview.getByRole("button", { name: "Next" });
  }
}

